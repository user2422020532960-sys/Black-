const delay = (ms) => new Promise(res => setTimeout(res, ms));

const activeMurgi = new Map();

module.exports = {
  config: {
    name: "مورجي",
    aliases: ["murgi"],
    version: "1.1.0",
    role: 2,
    author: "Saint Edit",
    description: "إرسال رسائل متكررة حتى يتم إيقافه",
    category: "fun",
    usages: "@mention | off",
    cooldowns: 5,
  },

  onStart: async function({ message, event, args }) {
    if (args[0] && args[0].toLowerCase() === "off") {
      if (activeMurgi.has(event.threadID)) {
        activeMurgi.set(event.threadID, false);
        return message.reply("〔⊘〕 تم إيقاف وضع مورجي.");
      } else {
        return message.reply("〔!〕 وضع مورجي لا يعمل حالياً.");
      }
    }

    const mention = Object.keys(event.mentions)[0];
    if (!mention) {
      return message.reply("〔!〕 يرجى الإشارة إلى هدف أولاً!");
    }

    const name = event.mentions[mention];
    const arraytag = [{ id: mention, tag: name }];

    const messages = [
      "ابن الكلب، هل أنت مستعد لتلقي ما تستحق؟",
      "الآن سأنام على حساب أمك وفي الصباح سأذهب لأباك، تصبح على خير يا صغير",
      "اسمع يا ابن الشارع، موت وأنت تعمل هذه الأشياء وانشرها واجعلني مشهوراً",
      "بالشتائم جعلت من نفسك رخيصاً",
      "يا هذا اليهودي الصغير، هل تستطيع فعل شيء غير ذلك؟",
      "─────يا أعمى، ما هذه الرائحة النتنة، كيف خرجت من هناك؟ عد إلى حيث أتيت",
      "─────إلى متى ستسير باسم أبيك يا ابن الفقير؟",
      "─────يا ابن المعدمين، ساكن الأكواخ",
      "─────أبوك ليس بذي قيمة حتى يقطع شعرة من قاع شرفي",
      "أمك لن تتأثر بكل شيء، لأنها صنعت من حديد",
      "────أكبر غلطة في حياتي أنني اقتربت من أمك، وبسبب هذه الغلطة وُلد نموذج مثلك",
      "⎯⎯يا ابن الشارع، إن لم ترد على رسائلي سأعاقبك بطريقتي",
      "─── شعلة الأبد، أريد أن أضع حداً لأمك، ماذا تقول؟",
      "يا ابن الخيانة، استقم قبل فوات الأوان، يا ابن الطاغية، أصبحت الشتائم عادتك",
      "سأنشر صوراً لأمك غداً",
      "يا ابن الخيانة، استقم قبل فوات الأوان، يا ابن الطاغية، أصبحت الشتائم عادتك",
      "سأسلط عليها القطط والكلاب يا ابن الفاسدة",
      "────أكبر غلطة في حياتي أنني اقتربت من أمك، وبسبب هذه الغلطة وُلد نموذج مثلك",
      "─── يا ابن الشارع، سأفضح أمرك أمام الجميع، يا ابن النجاسة",
      "سأسلط عليها الجميع يا ابن الفاسدة",
      "────يا ابن الوضيع، أضع حداً لكرامتك",
      "─── يا فاسق، أمك ليست بأفضل مما تظن",
      "─── يا ابن الفاسدة، سأعاقبك بما تستحق",
      "يا ابن الخيانة، أمك لا قيمة لها",
      "─── يا وضيع، أضع حداً لك",
      "يا ابن الكذب، سأفضح أمرك",
      "─── يا فاسق، سأعاقبك أمام الجميع",
      "يا ابن الوضاعة، استقم قبل فوات الأوان",
      "─── يا ابن الخيانة، كرامتك لا قيمة لها",
      "يا ابن الوضيع، سأجعلك مثار سخرية الجميع",
      "─── يا فاسق، استقم قبل أن يفوت الأوان",
      "يا ابن النجاسة، أضع حداً لأمرك",
      "─── يا ابن الشارع، سأعاقبك بما تستحق",
      "يا ابن الكذب، لا كرامة لك",
      "─── يا وضيع، سأفضح أمرك أمام الجميع",
      "يا ابن الخيانة، أمك مصدر خزيك",
      "─── يا فاسق، أنت لا تساوي شيئاً",
      "يا ابن الوضاعة، سأعاقبك بما تستحق",
      "─── يا ابن النجاسة، كرامتك بلا قيمة",
      "يا ابن الكذب، استقم قبل فوات الأوان"
    ];

    activeMurgi.set(event.threadID, true);
    message.reply("〔◆〕 تم تفعيل وضع مورجي.");

    try {
      while (activeMurgi.get(event.threadID)) {
        for (const msg of messages) {
          if (!activeMurgi.get(event.threadID)) break;

          await delay(2500);
          message.reply({
            body: `${name}\n${msg}`,
            mentions: arraytag
          });
        }
      }
    } catch (err) {
      console.error(err);
      activeMurgi.delete(event.threadID);
      message.reply("〔✗〕 حدث خطأ ما!");
    }
  }
};
